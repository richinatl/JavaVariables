package VariablesDemo_;

public class VariablesDemo_ {

	public static void main(String[] args) {
		// initialize and display various variable types
		
	    int num1 = 0;
        System.out.println(num1);
        
        String sample = "This is a string";
        System.out.println(sample);
        
        Boolean truth = true;
        System.out.println(truth);
        
        float float1 = 24.34f;
        System.out.println(float1);
        
        double double1 = 24.304589;
        System.out.println(double1);
        
        int[] anArray = {1,2,3,4};
        System.out.println(anArray[3]);
        
        String[] foodArr = {"tacos", "pizza", "wings"};
        System.out.println(foodArr[0]);

	}

}
